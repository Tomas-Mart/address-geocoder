package com.example.geocoder.domain.model;

/**
 * Статусы обработки геокодирования
 * Используется в доменной модели и для сохранения в БД
 */
public enum ProcessingStatus {
    /**
     * Успешная обработка - оба API вернули валидные координаты
     */
    SUCCESS,

    /**
     * Частичный успех - один из API вернул ошибку, но второй сработал
     * Используется, если требуется получить хотя бы один результат
     */
    PARTIAL_SUCCESS,

    /**
     * Ошибка - ни один API не вернул валидные координаты
     */
    FAILED
}